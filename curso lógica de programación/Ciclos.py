# CICLO FOR (Para)
for i in range(0, 5, 1):       # Función range (valor inicial, i < 5 valor final, incremente/decremento)
    print(i, "Dulce")          # proceso


for i in range(2, 101, 2):       
    print(i, "Dulce")


'''
PRUEBA DE ESCRITORIO
               VARIABLE    PROCESO
                   i        imprimir
valor inicial      0
                   1        Dulce
                   2        Dulce
                   3        Dulce
                   4        Dulce
                   5
valor final
'''



# CICLO WHILE (Mientras)
j = 9                  # valor inicial
while(j > 0):          # condición final ( j >= 1)
  print(j, "Eduardo")  # proceso
  j = j - 2            # OPERADOR DE incremento / decremento (j += 1)

