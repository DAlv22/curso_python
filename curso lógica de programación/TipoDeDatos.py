# EJEMPLO 3. TIPO DE DATOS
# Se utilizan para clasificar los datos 

# int, float (Numericos), string (Texto), bool (logico booleano), list (lista), tuple

nombre = "Dulce"
edad = 20
estatura = 1.63
teléfono = "552634534"
dirección = "Calle X, Col Y, C.P. Z"
