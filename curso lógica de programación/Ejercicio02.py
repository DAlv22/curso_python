# Ejercicio 02
# Calcular el área y perímetro de un círculo

# ENTRADA DE DATOS
radio = 10
pi = 3.1416


# PROCESOS
área = pi * (radio ** 2)
perímetro = (2 * pi) * radio

# SALIDA DE DATOS
print("El área del circulo es de =", round (área, 2))
print("El perímetro del circulo es de =", round (perímetro, 2))






